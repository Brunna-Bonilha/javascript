//-----------------------Pré Incrementar---------------------//

let x = 10;
let y = 8;

console.log("\nPré Incrementar: x = ++ y");

x = ++y;

console.log("\nValor de X:" + x);
console.log("\nValor de Y:" + y);

//Ele resolve o y primeiro adicionando um numero e depois atribui ao x//

//------------------Pré Decrementar-----------------//

let x = 10;
let y = 8;

console.log("\nPré Decrementar: x = --y");

x = --y;

console.log("\nValor de X:" + x);
console.log("\nValor de Y:" + y);

//Ele resolve o valor de y subtraindo um numero e depois atribui ao x//

 //-------------------- Pós Incrementar--------------//

 let i = 40;
 let j = 30;

 console.log("\nPós Incrementar: i = j ++");

 i = j++

 console.log("\nValor de I:" + i);
console.log("\nValor de J:" + j);

